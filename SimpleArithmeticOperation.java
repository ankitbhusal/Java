// Write a Program to add, subtract, multiply and divide two numbers.
public class SimpleArithmeticOperation {
    public static void main (String[] args){
        int a = 20 ;
        int b = 10 ;
        int add = a + b ;
        int sub = a - b ;
        int mul = a * b ;
        float div = a / b ;
        System.out.println("Addition a + b is: " + add);
        System.out.println("Subtraction a - b is: " + sub);
        System.out.println("Multiplication a * b is: " + mul);
        System.out.println("Division a / b is: " + div);
    }
}    